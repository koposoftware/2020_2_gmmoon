package kr.ac.hanalife.consulting.reply.vo;

public class ConsultingReplyVO {
	
	private int		no;
	private int		cs_serialno;
	private	int		empno;
	private	int		cusno;
	private	String	name;
	private	String	type;
	private	String	content;
	private	String	date;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getCs_serialno() {
		return cs_serialno;
	}
	public void setCs_serialno(int cs_serialno) {
		this.cs_serialno = cs_serialno;
	}
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public int getCusno() {
		return cusno;
	}
	public void setCusno(int cusno) {
		this.cusno = cusno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	

}
